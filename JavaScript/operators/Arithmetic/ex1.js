document.write(`<br>mult of ${fno} and ${sno} is ${fno*sno}`);
document.write(`<br>div of ${fno} and ${sno} is ${fno/sno}`);
document.write(`<br>mod of ${fno} and ${sno} is ${fno%sno}`);